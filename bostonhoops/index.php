					<h3>Documenting The Courts</h3>
					<p>Research was the first step. I got on my bike and crisscrossed the city of Boston shooting hoops at over twenty different courts to start. Even though all of the details haven't yet found their way into version one of Boston Hoops I was taking into account things like court conditions, ease of access and any unique perks to the facilities. So far no two hoops are exactly alike which seems fitting since each court has its own distinctive personality.</p>



					<img class="img-full section-start" src="/_img/hoop-3-2.gif" alt="Boston Hoops Documentation" />

					<h3>Process</h3>
					<p>After getting my notes and doing some field research I had enough to start illustrating the hoops. All of the scuffs on the backboard are included, the different details of each rim are accounted for, the types of nets and backboards are here too. A considerable amount of time was spent designing a simple yet fun web site to easily display the different hoops and catalog their locations. From here I spent about a week nailing down the visual style and the color palette. The final direction felt like a good balance between cohesiveness, future expansion and flexibility.</p>

