					<h3>Sales and Inventory</h3>
					<p>Ahhhh—this is the fun part! Inventory for seven consecutive shows and for over 100 artists can make a person feel like they haven't seen the light of day for weeks (well, it's partly true). This is the least glamourous part of producing this event but it can also be the most rewarding when you get to meet a-hundred-or-more showgoers each night at the sales table. I love sharing our enthusiasm for posters and taking notes on how we could put on an even better show the nex time around. Running a table for four or five hours straight after hanging an entire show can feel like you're running a race but it's totally worth it!</p>


					<h3>Curation</h3>
					<p>Booking one show typically takes a few months of consistent work before the one-night-only event kicks off. Part of the labor-intensive duties include wrangling over 100 arists from all of the country and coordinating the shipment of hand selected pieces (both by the artists and myself). To make it into the show work just has to be printed by hand. We're not exclusionary either—everyone is invited from the heavy hitters in the poster world to the folks just starting out. Our only limitations are typically wall space and room in the van.</p>


